    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @EJB
    private ProductFacadeLocal pfl;
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession(false);

        if (session == null || session.getAttribute("user") == null) {
            response.sendRedirect("login.jsp");
            return;
        }

        User user = (User) session.getAttribute("user");
        String productIdStr = request.getParameter("productId");

        if (productIdStr == null) {
            response.sendRedirect("ViewProductsServlet.do");
            return;
        }

        try {
            Long productId = Long.parseLong(productIdStr);
            Product product = pfl.find(productId);

            if (product != null && product.getQuantity() > 0) {
                // Add to cart (Session-based cart)
                List<Product> cart = CartManager.getCart(session);
                CartManager.addToCart(cart, product);

            }

        } catch (NumberFormatException e) {
            // Log error if needed
        }
        
        RequestDispatcher dispatcher =request.getRequestDispatcher("browseProducts.jsp");
        dispatcher.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

